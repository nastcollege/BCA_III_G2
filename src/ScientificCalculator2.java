public class ScientificCalculator2 {


    //square
   static void square(double n) {
        System.out.println("Square:" + (n * n));
    }

    //cube
   static void cube(double n) {
        System.out.println("Cube:" + (n * n * n));
    }

    //add
    static void add(double m, double n) {
        System.out.println("Sum:" + (m + n));
    }

    //sub
    static void sub(double m, double n) {
        System.out.println("Difference:" + (m - n));
    }

    //mul
    static void multiple(double m, double n) {
        System.out.println("Multiple:" + (m * n));
    }

    //div
    public  static void division(double m, double n) {
        System.out.println("Division:" + (m / n));
    }



}
